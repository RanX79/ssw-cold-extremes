# Main workflow
# ---------------------------------------------------------------------
def main():
    """Run the selected Figure 1 workflow."""
    run_mode = RUN_MODE.upper()

    if run_mode not in ("RAW", "DETRENDED", "PLOT"):
        raise ValueError(
            "RUN_MODE must be 'RAW', 'DETRENDED', or 'PLOT'."
        )

    print("=" * 70)
    print(f"SSW source: {SSW_SOURCE}")
    print(f"Run mode: {run_mode}")
    print(f"Output directory: {OUTPUT_DIR}")
    print("=" * 70)

    ssw_df = read_ssw_events()

    if run_mode == "PLOT":
        raw_results, lat_values, lon_values = load_results(NPZ_RAW)
        detrended_results, _, _ = load_results(NPZ_DET)

        plot_combined_figure(
            raw_results,
            detrended_results,
            lat_values,
            lon_values,
        )

        del raw_results, detrended_results, lat_values, lon_values, ssw_df
        gc.collect()

        print("\nFinished.")
        return

    baseline_climatology, lat_values, lon_values = (
        build_baseline_climatology()
    )

    if run_mode == "RAW":
        if NPZ_RAW.exists():
            print(f"\nRaw cache already exists: {NPZ_RAW}")
        else:
            raw_results = calculate_composites(
                ssw_df,
                baseline_climatology,
                lat_values,
                lon_values,
                detrend=False,
            )

            save_results(
                NPZ_RAW,
                raw_results,
                lat_values,
                lon_values,
            )

            del raw_results
            gc.collect()

        print("\nRAW workflow complete.")
        print("Set RUN_MODE = 'DETRENDED' and run the script again.")

    elif run_mode == "DETRENDED":
        if NPZ_DET.exists():
            print(f"\nDetrended cache already exists: {NPZ_DET}")
        else:
            detrended_results = calculate_composites(
                ssw_df,
                baseline_climatology,
                lat_values,
                lon_values,
                detrend=True,
            )

            save_results(
                NPZ_DET,
                detrended_results,
                lat_values,
                lon_values,
            )

            del detrended_results
            gc.collect()

        print("\nDETRENDED workflow complete.")
        print("Set RUN_MODE = 'PLOT' and run the script again.")

    del ssw_df, baseline_climatology, lat_values, lon_values
    gc.collect()

    print("\nFinished.")


if __name__ == "__main__":
    main()
