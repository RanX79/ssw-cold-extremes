def get_var_name(ds, candidates):
    """Identify the 2-m temperature variable in a dataset."""
    for variable_name in candidates:
        if variable_name in ds.data_vars:
            return variable_name

    if len(ds.data_vars) == 1:
        return list(ds.data_vars)[0]

    raise ValueError(
        f"Cannot identify T2m variable. Available variables: "
        f"{list(ds.data_vars)}"
    )


def get_lat_lon_dim_names(da):
    """Return latitude and longitude dimension names."""
    lat_candidates = [dim for dim in da.dims if "lat" in dim.lower()]
    lon_candidates = [dim for dim in da.dims if "lon" in dim.lower()]

    if not lat_candidates:
        raise ValueError(f"Latitude dimension not found: {da.dims}")
    if not lon_candidates:
        raise ValueError(f"Longitude dimension not found: {da.dims}")

    return lat_candidates[0], lon_candidates[0]


def get_time_dim_name(da):
    """Return the time dimension name."""
    for name in ("valid_time", "time", "forecast_period"):
        if name in da.dims:
            return name

    raise ValueError(
        f"Time dimension not found. Dimensions: {da.dims}; "
        f"coordinates: {list(da.coords)}"
    )


def ensure_member_dim_first(da):
    """Move the ensemble-member dimension to the first position."""
    if "number" not in da.dims:
        raise ValueError(f"Missing ensemble-member dimension 'number': {da.dims}")

    return da.transpose("number", *[dim for dim in da.dims if dim != "number"])


def safe_member_label(number_coord, index):
    """Return an integer member label where possible."""
    try:
        return int(number_coord[index])
    except (TypeError, ValueError):
        return index + 1


def month_day_key(timestamp):
    """Return calendar date as MM-DD."""
    timestamp = pd.Timestamp(timestamp)
    return f"{timestamp.month:02d}-{timestamp.day:02d}"


def convert_lon_360_to_180(longitude):
    """Convert a longitude from 0-360 to -180-180 if needed."""
    return longitude - 360 if longitude > 180 else longitude


def add_region_boxes(ax):
    """Draw boxes for the three regional averages."""
    for region_box in REGION_BOXES.values():
        lon_min = convert_lon_360_to_180(region_box["lon_min"])
        lon_max = convert_lon_360_to_180(region_box["lon_max"])

        edges = [
            (
                [lon_min, lon_max],
                [region_box["lat_min"], region_box["lat_min"]],
            ),
            (
                [lon_min, lon_max],
                [region_box["lat_max"], region_box["lat_max"]],
            ),
            (
                [lon_min, lon_min],
                [region_box["lat_min"], region_box["lat_max"]],
            ),
            (
                [lon_max, lon_max],
                [region_box["lat_min"], region_box["lat_max"]],
            ),
        ]

        for x_values, y_values in edges:
            ax.plot(
                x_values,
                y_values,
                color="black",
                lw=1.4,
                transform=ccrs.PlateCarree(),
                zorder=6,
            )


