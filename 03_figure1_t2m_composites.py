def plot_combined_figure(raw_results, detrended_results, lat_values, lon_values):
    """Plot the Figure 1 raw and detrended T2m composite panels."""
    print(f"\nCreating figure: {PNG_OUT}")

    lat_full = lat_values.copy()
    lon = lon_values.copy()
    n_lat = len(lat_values)

    lat_threshold_index = np.argmax(lat_full < LAT_THRESHOLD)

    if lat_threshold_index == 0 and lat_full[0] < LAT_THRESHOLD:
        lat_threshold_index = n_lat

    norm = TwoSlopeNorm(vmin=-2, vcenter=0.0, vmax=2)
    n_rows = len(DECADE_PERIODS)
    n_cols = 2

    fig = plt.figure(figsize=(13, 2.5 * n_rows))
    contour_reference = None

    panel_labels = [
        "(a)", "(b)", "(c)", "(d)", "(e)",
        "(f)", "(g)", "(h)", "(i)", "(j)",
    ]

    for row_index, (_, _, period_label) in enumerate(DECADE_PERIODS):
        panel_sources = (
            (raw_results, "Raw T2m"),
            (detrended_results, "Detrended T2m"),
        )

        for column_index, (results, column_title) in enumerate(panel_sources):
            ax = fig.add_subplot(
                n_rows,
                n_cols,
                row_index * n_cols + column_index + 1,
                projection=ccrs.PlateCarree(),
            )

            composite = results[period_label]["composite"].copy()
            composite[lat_threshold_index:] = np.nan

            cyclic_composite, cyclic_lon = add_cyclic_point(
                composite,
                coord=lon,
            )

            lon_grid, lat_grid = np.meshgrid(cyclic_lon, lat_full)

            contour_reference = ax.pcolormesh(
                lon_grid,
                lat_grid,
                cyclic_composite,
                transform=ccrs.PlateCarree(),
                cmap=plt.cm.RdBu_r,
                norm=norm,
                shading="auto",
            )

            ax.coastlines(linewidth=0.8)
            ax.set_extent(
                [-180, 180, LAT_THRESHOLD, 90],
                crs=ccrs.PlateCarree(),
            )

            gridlines = ax.gridlines(
                draw_labels=True,
                linestyle="--",
                alpha=0.4,
                linewidth=0.4,
            )

            gridlines.xlocator = plt.MultipleLocator(60)
            gridlines.ylocator = ticker.FixedLocator([30, 50, 70])
            gridlines.top_labels = False
            gridlines.right_labels = False
            gridlines.left_labels = column_index == 0
            gridlines.bottom_labels = row_index == n_rows - 1
            gridlines.xlabel_style = {"size": 10}
            gridlines.ylabel_style = {"size": 10}

            if row_index == 0:
                add_region_boxes(ax)

            not_significant = results[period_label]["notsig"].astype(float)
            not_significant[lat_threshold_index:] = np.nan
            not_significant[not_significant == 0] = np.nan

            cyclic_not_significant, _ = add_cyclic_point(
                not_significant,
                coord=lon,
            )

            ax.contourf(
                lon_grid,
                lat_grid,
                cyclic_not_significant,
                levels=[0.5, 1.5],
                hatches=["////"],
                colors="none",
                transform=ccrs.PlateCarree(),
            )

            panel_index = row_index * n_cols + column_index

            ax.text(
                0.02,
                0.98,
                panel_labels[panel_index],
                transform=ax.transAxes,
                ha="left",
                va="top",
                fontsize=12,
                fontweight="bold",
                bbox={
                    "facecolor": "white",
                    "edgecolor": "none",
                    "alpha": 0.75,
                    "pad": 1.5,
                },
                zorder=10,
            )

            if row_index == 0:
                ax.text(
                    0.5,
                    1.25,
                    column_title,
                    transform=ax.transAxes,
                    ha="center",
                    va="bottom",
                    fontsize=13,
                    fontweight="bold",
                )

            event_count = results[period_label]["count"]

            ax.set_title(
                f"{period_label} (n={event_count})",
                fontsize=12,
                fontweight="bold",
                pad=4,
            )

    fig.subplots_adjust(right=0.88, hspace=-0.6, wspace=0.08)

    colorbar_axis = fig.add_axes([0.90, 0.30, 0.015, 0.40])
    colorbar = fig.colorbar(contour_reference, cax=colorbar_axis)
    colorbar.set_label(
        "T2m anomaly (K)",
        fontsize=12,
        fontweight="bold",
    )

    plt.savefig(PNG_OUT, dpi=300, bbox_inches="tight")
    plt.close(fig)

    gc.collect()

    print(f"Saved figure: {PNG_OUT}")


