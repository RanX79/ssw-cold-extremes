def save_results(npz_path, results, lat_values, lon_values):
    """Save composite fields and metadata in a compressed NPZ archive."""
    save_dict = {
        "lat": lat_values,
        "lon": lon_values,
    }

    for _, _, label in DECADE_PERIODS:
        save_dict[f"{label}__comp"] = results[label]["composite"]
        save_dict[f"{label}__count"] = np.array(results[label]["count"])
        save_dict[f"{label}__notsig"] = results[label]["notsig"]

    np.savez(npz_path, **save_dict)
    print(f"Saved cache: {npz_path}")


def load_results(npz_path):
    """Load a saved composite archive."""
    if not npz_path.exists():
        raise FileNotFoundError(
            f"Required cache was not found:\n  {npz_path}\n\n"
            "Run first with RUN_MODE='RAW' or RUN_MODE='DETRENDED'."
        )

    data = np.load(npz_path, allow_pickle=False)

    lat_values = data["lat"]
    lon_values = data["lon"]
    results = {}

    for _, _, label in DECADE_PERIODS:
        results[label] = {
            "composite": data[f"{label}__comp"],
            "count": int(data[f"{label}__count"]),
            "notsig": data[f"{label}__notsig"],
        }

    print(f"Loaded cache: {npz_path}")

    return results, lat_values, lon_values


