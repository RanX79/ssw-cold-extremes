# Baseline climatology
# ---------------------------------------------------------------------
def build_baseline_climatology():
    """Build an 11-day-smoothed ensemble-mean daily climatology."""
    print(
        f"\nBuilding baseline climatology: "
        f"{BASELINE_START}-{BASELINE_END}"
    )

    day_of_year_fields = {}
    lat_values = None
    lon_values = None

    for year in range(BASELINE_START, BASELINE_END + 1):
        result = load_one_year(year)

        if result is None:
            continue

        (
            t2m_values,
            times,
            _,
            _,
            _,
            _,
            year_lat_values,
            year_lon_values,
        ) = result

        if lat_values is None:
            lat_values = year_lat_values
            lon_values = year_lon_values

        ensemble_mean = t2m_values.mean(axis=0)
        day_of_years = pd.to_datetime(times).dayofyear.values

        for index, day_of_year in enumerate(day_of_years):
            day_of_year_fields.setdefault(day_of_year, []).append(
                ensemble_mean[index].astype(np.float32)
            )

        del t2m_values, times, ensemble_mean, day_of_years
        gc.collect()

    if not day_of_year_fields or lat_values is None or lon_values is None:
        raise RuntimeError(
            "No baseline data were loaded. Check T2M_DIR and "
            "BASELINE_START/BASELINE_END."
        )

    print("  Building raw daily climatology")

    n_lat = len(lat_values)
    n_lon = len(lon_values)
    n_days = 366

    climatology_raw = np.full(
        (n_days, n_lat, n_lon),
        np.nan,
        dtype=np.float32,
    )

    for day_of_year, fields in day_of_year_fields.items():
        climatology_raw[day_of_year - 1] = np.nanmean(
            np.stack(fields, axis=0),
            axis=0,
        )

    del day_of_year_fields
    gc.collect()

    print("  Applying 11-day circular smoothing")

    window = 11
    padding = window // 2

    climatology_padded = np.concatenate(
        [
            climatology_raw[-padding:],
            climatology_raw,
            climatology_raw[:padding],
        ],
        axis=0,
    )

    climatology_smoothed = np.full_like(climatology_raw, np.nan)

    for index in range(n_days):
        climatology_smoothed[index] = np.nanmean(
            climatology_padded[index:index + window],
            axis=0,
        )

    del climatology_padded, climatology_raw
    gc.collect()

    baseline_climatology = {}

    for date in pd.date_range("2001-01-01", "2001-12-31"):
        key = month_day_key(date)
        baseline_climatology[key] = climatology_smoothed[
            date.dayofyear - 1
        ]

    del climatology_smoothed
    gc.collect()

    print(
        f"  Baseline climatology complete: "
        f"{len(baseline_climatology)} calendar-day fields"
    )

    return baseline_climatology, lat_values, lon_values


def build_anomaly_field(t2m_values, monthday, baseline_climatology):
    """Calculate daily T2m anomalies from the baseline climatology."""
    _, n_time, n_lat, n_lon = t2m_values.shape

    climatology = np.full(
        (n_time, n_lat, n_lon),
        np.nan,
        dtype=np.float32,
    )

    for index, key in enumerate(monthday):
        if key in baseline_climatology:
            climatology[index] = baseline_climatology[key]

    anomalies = t2m_values - climatology[None, :, :, :]

    del climatology
    return anomalies


