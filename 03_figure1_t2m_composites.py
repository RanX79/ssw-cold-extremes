# Composite calculations
# ---------------------------------------------------------------------
def extract_samples_from_year(
    year,
    anomalies,
    member_to_index,
    time_to_index,
    ssw_df,
    day_start,
    day_end,
    period_samples,
):
    """Extract one day-15 to day-59 mean field for every valid SSW event."""
    n_time = anomalies.shape[1]

    period_label = None

    for start_year, end_year, label in DECADE_PERIODS:
        if start_year <= year <= end_year:
            period_label = label
            break

    if period_label is None:
        return

    events_this_year = ssw_df[
        ssw_df["init_year"] == year
    ]

    for _, row in events_this_year.iterrows():
        member = int(row["member"])
        onset_date = pd.Timestamp(row["ssw_date"]).normalize()

        if member not in member_to_index:
            continue

        if onset_date not in time_to_index:
            continue

        member_index = member_to_index[member]
        onset_index = time_to_index[onset_date]

        start_index = onset_index + day_start
        end_index = onset_index + day_end + 1

        if start_index < 0 or end_index > n_time:
            continue

        event_mean = np.nanmean(
            anomalies[member_index, start_index:end_index],
            axis=0,
        ).astype(np.float32)

        period_samples[period_label].append(event_mean)


def calculate_composites(
    ssw_df,
    baseline_climatology,
    lat_values,
    lon_values,
    detrend=False,
):
    """Calculate raw or detrended SSW-composite fields."""
    mode_name = "detrended" if detrend else "raw"

    print("\n" + "=" * 70)
    print(f"Calculating {mode_name} composites")
    print("=" * 70)

    n_lat = len(lat_values)
    n_lon = len(lon_values)
    day_start, day_end, _ = COMBINED_WINDOW

    period_samples = {
        label: []
        for _, _, label in DECADE_PERIODS
    }

    trend_parameters = None
    trend_reference_year = None

    if detrend:
        print("  Pass 1 of 2: calculating daily trend parameters")

        calendar_day_entries = {}
        usable_years = []

        for year in range(START_YEAR, END_YEAR + 1):
            result = load_one_year(year)

            if result is None:
                continue

            t2m_values, _, monthday, _, _, _, _, _ = result

            anomalies = build_anomaly_field(
                t2m_values,
                monthday,
                baseline_climatology,
            )
            ensemble_mean = anomalies.mean(axis=0)

            for index, key in enumerate(monthday):
                calendar_day_entries.setdefault(key, []).append(
                    (year, ensemble_mean[index].astype(np.float32))
                )

            usable_years.append(year)

            del t2m_values, anomalies, ensemble_mean, monthday
            gc.collect()

        if not usable_years:
            raise RuntimeError("No SEAS5 data available for detrending.")

        trend_reference_year = float(np.mean(usable_years))
        trend_parameters = {}

        for key, entries in calendar_day_entries.items():
            if len(entries) < 2:
                continue

            years = np.array(
                [entry[0] for entry in entries],
                dtype=np.float64,
            )
            centered_years = years - years.mean()

            fields = np.stack(
                [entry[1] for entry in entries],
                axis=0,
            ).astype(np.float64)

            denominator = np.sum(centered_years ** 2)

            if denominator == 0:
                continue

            slope = (
                centered_years[:, None, None] * fields
            ).sum(axis=0) / denominator

            intercept_at_mean_year = fields.mean(axis=0)

            trend_parameters[key] = (
                intercept_at_mean_year.astype(np.float32),
                slope.astype(np.float32),
            )

            del years, centered_years, fields, slope, intercept_at_mean_year

        del calendar_day_entries
        gc.collect()

        print(
            f"  Trend parameters calculated for "
            f"{len(trend_parameters)} calendar days"
        )

        print("  Pass 2 of 2: extracting detrended composites")

    for year in range(START_YEAR, END_YEAR + 1):
        result = load_one_year(year)

        if result is None:
            continue

        (
            t2m_values,
            _,
            monthday,
            _,
            member_to_index,
            time_to_index,
            _,
            _,
        ) = result

        anomalies = build_anomaly_field(
            t2m_values,
            monthday,
            baseline_climatology,
        )

        if detrend:
            for index, key in enumerate(monthday):
                if key not in trend_parameters:
                    continue

                intercept_at_mean_year, slope = trend_parameters[key]
                trend_value = (
                    intercept_at_mean_year
                    + slope * (year - trend_reference_year)
                )

                anomalies[:, index] -= trend_value[None, :, :]

        extract_samples_from_year(
            year=year,
            anomalies=anomalies,
            member_to_index=member_to_index,
            time_to_index=time_to_index,
            ssw_df=ssw_df,
            day_start=day_start,
            day_end=day_end,
            period_samples=period_samples,
        )

        del t2m_values, anomalies, monthday, member_to_index, time_to_index
        gc.collect()

    if detrend:
        del trend_parameters
        gc.collect()

    results = {}

    for _, _, label in DECADE_PERIODS:
        samples = period_samples[label]

        if samples:
            sample_stack = np.stack(samples, axis=0).astype(np.float32)
            composite = np.nanmean(sample_stack, axis=0).astype(np.float32)

            if sample_stack.shape[0] >= 2:
                _, p_values = ttest_1samp(
                    sample_stack,
                    popmean=0.0,
                    axis=0,
                    nan_policy="omit",
                )

                not_significant = (
                    (~np.isfinite(p_values))
                    | (p_values >= ALPHA_SIG)
                ).astype(np.int8)

                del p_values

            else:
                not_significant = np.ones(
                    (n_lat, n_lon),
                    dtype=np.int8,
                )

            sample_count = sample_stack.shape[0]

            del sample_stack

        else:
            composite = np.full(
                (n_lat, n_lon),
                np.nan,
                dtype=np.float32,
            )
            not_significant = np.ones(
                (n_lat, n_lon),
                dtype=np.int8,
            )
            sample_count = 0

        results[label] = {
            "composite": composite,
            "count": sample_count,
            "notsig": not_significant,
        }

        print(f"  Period {label}: {sample_count} event samples")

    del period_samples
    gc.collect()

    return results


