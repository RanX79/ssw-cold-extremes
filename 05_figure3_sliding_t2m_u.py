def build_baseline_climatology(
    years,
    daily_dir,
    file_pattern,
    var_candidates,
):
    """Build an ensemble-mean, 11-day-smoothed daily climatology."""
    print(
        f"  Building baseline climatology "
        f"{years[0]}-{years[-1]} with +/-5-day smoothing"
    )

    day_of_year_fields = {}
    lat_values = None
    lon_values = None

    for year in years:
        data = load_one_year_daily(
            year,
            daily_dir,
            file_pattern,
            var_candidates,
        )

        if data is None:
            continue

        if lat_values is None:
            lat_values = data["lat_vals"]
            lon_values = data["lon_vals"]

        ensemble_mean = data["arr"].mean(axis=0)
        day_of_years = pd.to_datetime(data["times"]).dayofyear.values

        for index, day_of_year in enumerate(day_of_years):
            day_of_year_fields.setdefault(day_of_year, []).append(
                ensemble_mean[index].astype(np.float32)
            )

        del data, ensemble_mean, day_of_years
        gc.collect()

    if not day_of_year_fields or lat_values is None or lon_values is None:
        raise RuntimeError(
            "No baseline daily files were loaded. Check input paths."
        )

    n_lat = len(lat_values)
    n_lon = len(lon_values)

    climatology_raw = np.full(
        (366, n_lat, n_lon),
        np.nan,
        dtype=np.float32,
    )

    for day_of_year, fields in day_of_year_fields.items():
        climatology_raw[day_of_year - 1] = np.nanmean(
            np.stack(fields, axis=0),
            axis=0,
        )

    del day_of_year_fields
    gc.collect()

    window = 11
    padding = window // 2

    climatology_padded = np.concatenate(
        [
            climatology_raw[-padding:],
            climatology_raw,
            climatology_raw[:padding],
        ],
        axis=0,
    )

    climatology_smoothed = np.full_like(climatology_raw, np.nan)

    for index in range(366):
        window_values = climatology_padded[index:index + window]

        if np.isfinite(window_values).any():
            climatology_smoothed[index] = np.nanmean(
                window_values,
                axis=0,
            )

    del climatology_padded, climatology_raw
    gc.collect()

    climatology = {}

    for timestamp in pd.date_range("2001-01-01", "2001-12-31"):
        climatology[month_day_key(timestamp)] = (
            climatology_smoothed[timestamp.dayofyear - 1]
        )

    del climatology_smoothed
    gc.collect()

    print(f"  Baseline climatology completed: {len(climatology)} days")

    return climatology, lat_values, lon_values


def build_trend_params(
    climatology,
    daily_dir,
    file_pattern,
    var_candidates,
):
    """
    Calculate cross-year linear trend coefficients for each calendar day.

    Parameters are calculated from ensemble-mean daily anomalies.
    """
    print("  Building daily trend parameters")

    calendar_day_anomalies = {}

    for year in range(START_YEAR, END_YEAR + 1):
        data = load_one_year_daily(
            year,
            daily_dir,
            file_pattern,
            var_candidates,
        )

        if data is None:
            continue

        ensemble_mean = data["arr"].mean(axis=0)

        for index, timestamp in enumerate(data["times"]):
            calendar_day = (timestamp.month, timestamp.day)

            climatology_field = climatology.get(
                month_day_key(timestamp)
            )

            if climatology_field is None:
                continue

            anomaly = (
                ensemble_mean[index] - climatology_field
            ).astype(np.float32)

            calendar_day_anomalies.setdefault(
                calendar_day,
                [],
            ).append((year, anomaly))

        del data, ensemble_mean
        gc.collect()

    trend_params = {}

    for calendar_day, entries in calendar_day_anomalies.items():
        if len(entries) < 2:
            continue

        year_values = np.array(
            [entry[0] for entry in entries],
            dtype=np.float64,
        )

        mean_year = float(year_values.mean())
        centered_years = year_values - mean_year
        denominator = np.sum(centered_years ** 2)

        if denominator < 1e-10:
            continue

        anomaly_stack = np.stack(
            [entry[1] for entry in entries],
            axis=0,
        ).astype(np.float64)

        slope = (
            centered_years[:, None, None] * anomaly_stack
        ).sum(axis=0) / denominator

        # Mean anomaly at mean_year; retained for cache consistency.
        intercept = anomaly_stack.mean(axis=0)

        trend_params[calendar_day] = (
            intercept.astype(np.float32),
            slope.astype(np.float32),
            mean_year,
        )

        del (
            year_values,
            centered_years,
            anomaly_stack,
            slope,
            intercept,
        )

    del calendar_day_anomalies
    gc.collect()

    print(f"  Trend parameters completed: {len(trend_params)} days")

    return trend_params


