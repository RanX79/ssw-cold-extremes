def compute_t2m_sliding(ssw_df):
    """Compute regional sliding-window T2m anomaly composites."""
    print("\n" + "=" * 70)
    print("T2m sliding-window pipeline")

    baseline_years = list(range(BASELINE_START, BASELINE_END + 1))
    file_pattern = "SEAS5_2mt_NH_{year}11_system51_m25_daily.nc"

    climatology, lat_values, lon_values = build_baseline_climatology(
        baseline_years,
        T2M_DAILY_DIR,
        file_pattern,
        T2M_VAR_CANDIDATES,
    )

    trend_params = build_trend_params(
        climatology,
        T2M_DAILY_DIR,
        file_pattern,
        T2M_VAR_CANDIDATES,
    )

    all_time_series = {}

    for region_name, region_box in REGION_BOXES.items():
        print(f"  Extracting region: {region_name}")

        def extract_function(
            field,
            field_lat_values,
            field_lon_values,
            selected_region=region_box,
        ):
            return cosine_weighted_mean(
                field,
                field_lat_values,
                field_lon_values,
                selected_region,
            )

        event_cache = extract_event_samples(
            ssw_df,
            climatology,
            trend_params,
            lat_values,
            lon_values,
            T2M_DAILY_DIR,
            file_pattern,
            T2M_VAR_CANDIDATES,
            extract_function,
        )

        all_time_series[region_name] = aggregate_sliding_windows(
            ssw_df,
            event_cache,
        )

        del event_cache
        gc.collect()

    del climatology, trend_params, lat_values, lon_values
    gc.collect()

    save_t2m_sliding_cache(all_time_series)

    return all_time_series


def save_t2m_sliding_cache(all_time_series):
    """Save regional T2m sliding-window composites as NPZ."""
    output = {}

    for region_name, time_series in all_time_series.items():
        for window_label, values in time_series.items():
            prefix = f"{region_name}__{make_tag(window_label)}"

            for key in ("xc", "wl", "rm", "rs", "dm", "ds", "rn"):
                value = values[key]

                if key == "wl":
                    value = np.array(value, dtype=str)

                output[f"{prefix}__{key}"] = value

    np.savez(NPZ_TS_T2M, **output)

    print(f"Saved T2m cache: {NPZ_TS_T2M}")


