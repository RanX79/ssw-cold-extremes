def cosine_weighted_mean(field_2d, lat_values, lon_values, region_box):
    """Calculate cosine-latitude-weighted regional mean T2m anomaly."""
    lat_mask = (
        (lat_values >= region_box["lat_min"])
        & (lat_values <= region_box["lat_max"])
    )
    lon_mask = (
        (lon_values >= region_box["lon_min"])
        & (lon_values <= region_box["lon_max"])
    )

    if not np.any(lat_mask) or not np.any(lon_mask):
        return np.nan

    subset = field_2d[np.ix_(lat_mask, lon_mask)]

    latitude_weights = np.cos(np.deg2rad(lat_values[lat_mask]))
    weights_2d = latitude_weights[:, None] * np.ones(lon_mask.sum())

    valid = np.isfinite(subset)

    if not np.any(valid):
        return np.nan

    return float(
        np.nansum(subset * weights_2d * valid)
        / np.nansum(weights_2d * valid)
    )


def zonal_mean_at_lat(field_2d, lat_values, lon_values, target_lat):
    """Calculate the zonal mean at the nearest latitude to target_lat."""
    del lon_values

    latitude_index = np.argmin(np.abs(lat_values - target_lat))
    row = field_2d[latitude_index, :]
    valid = np.isfinite(row)

    if not np.any(valid):
        return np.nan

    return float(np.nanmean(row[valid]))


