def plot_one_panel(
    ax,
    time_series,
    y_limits,
    xpad_fraction=0.02,
    trend_fontsize=11,
):
    """Plot raw and detrended sliding-window time series in one panel."""
    raw_label_items = []
    detrended_label_items = []

    for _, _, window_label in TIME_WINDOWS:
        values = time_series[window_label]

        x_centers = values["xc"]
        raw_means = values["rm"]
        raw_errors = values["rs"]
        detrended_means = values["dm"]
        detrended_errors = values["ds"]

        color = WIN_COLORS[window_label]

        ax.plot(
            x_centers,
            raw_means,
            color=color,
            lw=2.5,
            ls="-",
            zorder=4,
        )
        ax.fill_between(
            x_centers,
            raw_means - raw_errors,
            raw_means + raw_errors,
            color=color,
            alpha=0.12,
            zorder=2,
        )

        ax.plot(
            x_centers,
            detrended_means,
            color=color,
            lw=2.5,
            ls="--",
            zorder=4,
        )
        ax.fill_between(
            x_centers,
            detrended_means - detrended_errors,
            detrended_means + detrended_errors,
            color=color,
            alpha=0.07,
            zorder=2,
        )

        x_end, y_end, label = get_line_trend_label(
            x_centers,
            raw_means,
        )

        if label:
            raw_label_items.append(
                {
                    "x": x_end,
                    "y": y_end,
                    "label": f"{label} (R)",
                    "color": color,
                }
            )

        x_end, y_end, label = get_line_trend_label(
            x_centers,
            detrended_means,
        )

        if label:
            detrended_label_items.append(
                {
                    "x": x_end,
                    "y": y_end,
                    "label": f"{label} (D)",
                    "color": color,
                }
            )

    ax.axhline(0, color="black", lw=0.9, zorder=3)

    reference_window = TIME_WINDOWS[0][2]
    x_reference = time_series[reference_window]["xc"]
    window_labels = time_series[reference_window]["wl"]

    tick_step = 5
    tick_indices = np.arange(0, len(x_reference), tick_step)

    tick_labels = [
        window_labels[index].replace("-", "–")
        for index in tick_indices
    ]

    ax.set_xticks(x_reference[tick_indices])
    ax.set_xticklabels(
        tick_labels,
        rotation=35,
        ha="right",
        fontsize=12,
    )

    x_range = x_reference[-1] - x_reference[0]

    clipped_raw_labels = [
        {
            **item,
            "y": np.clip(item["y"], y_limits[0], y_limits[1]),
        }
        for item in raw_label_items
    ]

    clipped_detrended_labels = [
        {
            **item,
            "y": np.clip(item["y"], y_limits[0], y_limits[1]),
        }
        for item in detrended_label_items
    ]

    ax.set_xlim(x_reference[0] - 0.3, x_reference[-1] + 0.3)
    ax.set_ylim(y_limits)
    ax.autoscale(enable=False)
    ax.tick_params(axis="y", labelsize=11)

    add_right_side_labels(
        ax,
        clipped_raw_labels + clipped_detrended_labels,
        x_range * xpad_fraction,
        fontsize=trend_fontsize,
    )

    ax.set_xlim(x_reference[0] - 0.3, x_reference[-1] + 0.3)
    ax.set_ylim(y_limits)

    style_axis(ax)


