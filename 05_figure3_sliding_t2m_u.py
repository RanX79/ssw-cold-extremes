def load_one_year_daily(
    year,
    daily_dir,
    file_pattern,
    var_candidates,
    flip_lat=True,
    wrap_lon=True,
):
    """Load and standardize one daily SEAS5 file."""
    input_file = daily_dir / file_pattern.format(year=year)

    if not input_file.exists():
        print(f"  Missing input file: {input_file.name}")
        return None

    try:
        ds = xr.open_dataset(input_file)

        try:
            variable_name = get_var_name(ds, var_candidates)
            da = ds[variable_name].load()
        finally:
            ds.close()

        if "number" not in da.dims:
            raise ValueError(
                f"Ensemble-member dimension 'number' not found: {da.dims}"
            )

        da = da.transpose(
            "number",
            *[dimension for dimension in da.dims if dimension != "number"],
        )
        da = da.isel(number=slice(0, N_MEMBERS))

        for dimension in list(da.dims):
            is_non_spatial_singleton = (
                dimension != "number"
                and da.sizes[dimension] == 1
                and "lat" not in dimension.lower()
                and "lon" not in dimension.lower()
                and "time" not in dimension.lower()
                and "valid" not in dimension.lower()
            )

            if is_non_spatial_singleton:
                da = da.squeeze(dimension, drop=True)

        lat_name, lon_name = get_lat_lon_dim_names(da)
        time_name = get_time_dim_name(da)

        if flip_lat and da[lat_name].values[0] < da[lat_name].values[-1]:
            da = da.isel({lat_name: slice(None, None, -1)})

        if wrap_lon:
            original_lon = da[lon_name].values

            if np.nanmax(original_lon) > 180:
                converted_lon = np.where(
                    original_lon > 180,
                    original_lon - 360,
                    original_lon,
                )
                sort_index = np.argsort(converted_lon)

                da = da.isel({lon_name: sort_index})
                da = da.assign_coords(
                    {lon_name: converted_lon[sort_index]}
                )

        times = pd.to_datetime(da[time_name].values).normalize()

        member_labels = [
            safe_member_label(da["number"].values, index)
            for index in range(da.sizes["number"])
        ]

        values = da.values.astype(np.float32)
        lat_values = da[lat_name].values.copy()
        lon_values = da[lon_name].values.copy()

        return {
            "arr": values,
            "times": times,
            "monthday": np.array(
                [month_day_key(timestamp) for timestamp in times]
            ),
            "member_labels": member_labels,
            "member_to_idx": {
                member: index
                for index, member in enumerate(member_labels)
            },
            "time_to_idx": {
                pd.Timestamp(timestamp): index
                for index, timestamp in enumerate(times)
            },
            "lat_vals": lat_values,
            "lon_vals": lon_values,
        }

    except Exception as error:
        print(f"  Failed to load year {year}: {error}")
        gc.collect()
        return None


