def extract_event_samples(
    ssw_df,
    climatology,
    trend_params,
    lat_values,
    lon_values,
    daily_dir,
    file_pattern,
    var_candidates,
    extract_function,
):
    """
    Extract raw and detrended event-mean values for each SSW event and
    each defined time window.
    """
    event_cache = {
        window_label: {}
        for _, _, window_label in TIME_WINDOWS
    }

    for year in range(START_YEAR, END_YEAR + 1):
        events_this_year = ssw_df[
            ssw_df["init_year"] == year
        ]

        if events_this_year.empty:
            continue

        data = load_one_year_daily(
            year,
            daily_dir,
            file_pattern,
            var_candidates,
        )

        if data is None:
            continue

        values = data["arr"]
        monthday = data["monthday"]
        times = data["times"]
        member_to_index = data["member_to_idx"]
        time_to_index = data["time_to_idx"]

        n_time = values.shape[1]
        n_lat = values.shape[2]
        n_lon = values.shape[3]

        climatology_array = np.full(
            (n_time, n_lat, n_lon),
            np.nan,
            dtype=np.float32,
        )

        for index, key in enumerate(monthday):
            climatology_field = climatology.get(key)

            if climatology_field is not None:
                climatology_array[index] = climatology_field

        raw_anomalies = values - climatology_array[None, :, :, :]
        detrended_anomalies = raw_anomalies.copy()

        for index, timestamp in enumerate(times):
            calendar_day = (timestamp.month, timestamp.day)
            coefficients = trend_params.get(calendar_day)

            if coefficients is None:
                continue

            _, slope, mean_year = coefficients

            # Remove only the time-varying trend component so the
            # anomaly baseline itself remains unchanged.
            detrended_anomalies[:, index] -= (
                slope * (year - mean_year)
            )[None, :, :]

        del climatology_array
        gc.collect()

        for _, row in events_this_year.iterrows():
            member = int(row["member"])
            onset_date = pd.Timestamp(row["ssw_date"]).normalize()

            if member not in member_to_index:
                continue

            if onset_date not in time_to_index:
                continue

            member_index = member_to_index[member]
            onset_index = time_to_index[onset_date]

            for day_start, day_end, window_label in TIME_WINDOWS:
                start_index = onset_index + day_start
                end_index = onset_index + day_end + 1

                if start_index < 0 or end_index > n_time:
                    continue

                raw_field = np.nanmean(
                    raw_anomalies[
                        member_index,
                        start_index:end_index,
                    ],
                    axis=0,
                )

                detrended_field = np.nanmean(
                    detrended_anomalies[
                        member_index,
                        start_index:end_index,
                    ],
                    axis=0,
                )

                event_key = (
                    year,
                    member,
                    onset_date,
                )

                event_cache[window_label][event_key] = (
                    extract_function(
                        raw_field,
                        lat_values,
                        lon_values,
                    ),
                    extract_function(
                        detrended_field,
                        lat_values,
                        lon_values,
                    ),
                )

                del raw_field, detrended_field

        del values, raw_anomalies, detrended_anomalies, data
        gc.collect()

    return event_cache


def aggregate_sliding_windows(ssw_df, event_cache):
    """Aggregate event values into overlapping SLIDING_WINDOW-year means."""
    first_year = int(ssw_df["init_year"].min())
    last_year = int(ssw_df["init_year"].max())

    windows = [
        (year, year + SLIDING_WINDOW - 1)
        for year in range(
            first_year,
            last_year - SLIDING_WINDOW + 2,
        )
    ]

    time_series = {}

    for _, _, window_label in TIME_WINDOWS:
        cache = event_cache[window_label]

        x_centers = []
        window_labels = []
        raw_means = []
        raw_standard_errors = []
        detrended_means = []
        detrended_standard_errors = []
        sample_counts = []

        for start_year, end_year in windows:
            events_in_window = ssw_df[
                (ssw_df["init_year"] >= start_year)
                & (ssw_df["init_year"] <= end_year)
            ]

            raw_values = []
            detrended_values = []

            for _, row in events_in_window.iterrows():
                event_key = (
                    int(row["init_year"]),
                    int(row["member"]),
                    pd.Timestamp(row["ssw_date"]).normalize(),
                )

                if event_key not in cache:
                    continue

                raw_value, detrended_value = cache[event_key]
                raw_values.append(raw_value)
                detrended_values.append(detrended_value)

            sample_count = len(raw_values)

            raw_means.append(
                np.nanmean(raw_values)
                if sample_count > 0 else np.nan
            )
            detrended_means.append(
                np.nanmean(detrended_values)
                if sample_count > 0 else np.nan
            )

            raw_standard_errors.append(
                np.nanstd(raw_values, ddof=1) / np.sqrt(sample_count)
                if sample_count > 1 else np.nan
            )
            detrended_standard_errors.append(
                np.nanstd(detrended_values, ddof=1) / np.sqrt(sample_count)
                if sample_count > 1 else np.nan
            )

            sample_counts.append(sample_count)
            x_centers.append(
                start_year + (SLIDING_WINDOW - 1) / 2.0
            )
            window_labels.append(f"{start_year}-{end_year}")

        time_series[window_label] = {
            "xc": np.array(x_centers),
            "wl": window_labels,
            "rm": np.array(raw_means),
            "rs": np.array(raw_standard_errors),
            "dm": np.array(detrended_means),
            "ds": np.array(detrended_standard_errors),
            "rn": np.array(sample_counts),
        }

    return time_series


