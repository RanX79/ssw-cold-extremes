def block_bootstrap_trend(
    x_values,
    mean_values,
    n_boot=1000,
    block_size=3,
    seed=42,
):
    """
    Estimate a Theil-Sen trend and block-bootstrap p value.

    The bootstrap samples contiguous index blocks, preserving part of
    the serial dependence in overlapping sliding-window time series.
    """
    random_generator = np.random.default_rng(seed)
    n_values = len(mean_values)

    if n_values < 3:
        return np.nan, np.nan

    observed_slope = theilslopes(mean_values, x_values).slope
    bootstrap_slopes = []

    for _ in range(n_boot):
        indices = []

        while len(indices) < n_values:
            start = random_generator.integers(
                0,
                max(1, n_values - block_size + 1),
            )

            indices.extend(
                range(start, min(start + block_size, n_values))
            )

        sampled_indices = np.array(indices[:n_values])

        bootstrap_slopes.append(
            theilslopes(
                mean_values[sampled_indices],
                x_values,
            ).slope
        )

    bootstrap_slopes = np.array(bootstrap_slopes)
    centered_slopes = bootstrap_slopes - np.mean(bootstrap_slopes)

    p_value = float(
        np.mean(np.abs(centered_slopes) >= np.abs(observed_slope))
    )

    return observed_slope, p_value


def p_to_sig(p_value):
    """Return significance symbols for a p value."""
    if not np.isfinite(p_value):
        return ""

    if p_value < 0.01:
        return "**"

    if p_value < 0.05:
        return "*"

    return ""


def get_line_trend_label(x_values, y_values, fmt="{:+.2f}"):
    """Return end-point coordinates and a per-decade trend label."""
    valid = np.isfinite(y_values)

    if valid.sum() <= 5:
        return None, None, None

    x_valid = x_values[valid]
    y_valid = y_values[valid]

    trend_result = theilslopes(y_valid, x_valid)

    _, p_value = block_bootstrap_trend(
        x_valid,
        y_valid,
        n_boot=1000,
        block_size=3,
    )

    slope_per_decade = trend_result.slope * 10.0

    label = fmt.format(slope_per_decade) + p_to_sig(p_value)

    return x_valid[-1], y_valid[-1], label


def adjust_label_positions(label_items, ymin, ymax, min_gap):
    """Adjust right-side text labels vertically to reduce overlap."""
    if not label_items:
        return label_items

    sorted_items = sorted(label_items, key=lambda item: item["y"])

    sorted_items[0]["y_adj"] = sorted_items[0]["y"]

    for index in range(1, len(sorted_items)):
        sorted_items[index]["y_adj"] = max(
            sorted_items[index]["y"],
            sorted_items[index - 1]["y_adj"] + min_gap,
        )

    for _ in range(2):
        top_overflow = sorted_items[-1]["y_adj"] - ymax

        if top_overflow > 0:
            for item in sorted_items:
                item["y_adj"] -= top_overflow

        bottom_overflow = ymin - sorted_items[0]["y_adj"]

        if bottom_overflow > 0:
            for item in sorted_items:
                item["y_adj"] += bottom_overflow

        for index in range(len(sorted_items) - 2, -1, -1):
            maximum_position = (
                sorted_items[index + 1]["y_adj"] - min_gap
            )

            if sorted_items[index]["y_adj"] > maximum_position:
                sorted_items[index]["y_adj"] = maximum_position

    return sorted_items


def add_right_side_labels(ax, label_items, xpad, fontsize=11):
    """Add non-overlapping raw and detrended trend labels."""
    if not label_items:
        return

    ymin, ymax = ax.get_ylim()
    y_range = ymax - ymin

    adjusted_items = adjust_label_positions(
        label_items,
        ymin - 0.20 * y_range,
        ymax + 0.20 * y_range,
        min_gap=0.06 * y_range,
    )

    for item in adjusted_items:
        ax.text(
            item["x"] + xpad,
            item["y_adj"],
            item["label"],
            color=item["color"],
            fontsize=fontsize,
            ha="left",
            va="center",
            fontweight="bold",
            clip_on=False,
        )


