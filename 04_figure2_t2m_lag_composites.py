def build_lag_composites(
    ssw_df,
    baseline_climatology,
    trend_params,
    lat_values,
    lon_values,
):
    """Calculate raw and detrended regional lag composites."""
    print(f"\nBuilding lag composites from {LAGS[0]} to {LAGS[-1]} days")

    data_store = {
        tag: {
            region_name: {lag: [] for lag in LAGS}
            for region_name in REGION_ORDER
        }
        for tag in ("p1_raw", "p2_raw", "p1_det", "p2_det")
    }

    for year in range(START_YEAR, END_YEAR + 1):
        events_this_year = ssw_df[
            ssw_df["init_year"] == year
        ]

        if events_this_year.empty:
            continue

        data = load_one_year_daily(year)

        if data is None:
            continue

        t2m_values = data["t2m"]
        times = data["times"]
        member_to_index = data["member_to_idx"]
        time_to_index = data["time_to_idx"]

        n_time = t2m_values.shape[1]
        n_lat = t2m_values.shape[2]
        n_lon = t2m_values.shape[3]

        climatology = np.full(
            (n_time, n_lat, n_lon),
            np.nan,
            dtype=np.float32,
        )

        for index, timestamp in enumerate(times):
            climatology_field = baseline_climatology.get(
                month_day_key(timestamp)
            )

            if climatology_field is not None:
                climatology[index] = climatology_field

        raw_anomalies = t2m_values - climatology[None, :, :, :]
        detrended_anomalies = raw_anomalies.copy()

        for index, timestamp in enumerate(times):
            key = (timestamp.month, timestamp.day)
            coefficients = trend_params.get(key)

            if coefficients is None:
                continue

            intercept_at_mean_year, slope, mean_year = coefficients

            trend_value = (
                intercept_at_mean_year
                + slope * (year - mean_year)
            )

            detrended_anomalies[:, index] -= trend_value[None, :, :]

        del climatology
        gc.collect()

        in_period_1 = year in PERIOD1
        in_period_2 = year in PERIOD2

        for _, row in events_this_year.iterrows():
            member = int(row["member"])
            onset_date = pd.Timestamp(row["ssw_date"]).normalize()

            if member not in member_to_index:
                continue

            if onset_date not in time_to_index:
                continue

            member_index = member_to_index[member]
            onset_index = time_to_index[onset_date]

            for lag in LAGS:
                time_index = onset_index + lag

                if time_index < 0 or time_index >= n_time:
                    continue

                raw_field = raw_anomalies[member_index, time_index]
                detrended_field = detrended_anomalies[member_index, time_index]

                for region_name, region_box in REGION_BOXES.items():
                    raw_value = cosine_weighted_region_mean(
                        raw_field,
                        lat_values,
                        lon_values,
                        region_box,
                    )
                    detrended_value = cosine_weighted_region_mean(
                        detrended_field,
                        lat_values,
                        lon_values,
                        region_box,
                    )

                    if in_period_1:
                        if np.isfinite(raw_value):
                            data_store["p1_raw"][region_name][lag].append(
                                raw_value
                            )
                        if np.isfinite(detrended_value):
                            data_store["p1_det"][region_name][lag].append(
                                detrended_value
                            )

                    if in_period_2:
                        if np.isfinite(raw_value):
                            data_store["p2_raw"][region_name][lag].append(
                                raw_value
                            )
                        if np.isfinite(detrended_value):
                            data_store["p2_det"][region_name][lag].append(
                                detrended_value
                            )

        del data, t2m_values, raw_anomalies, detrended_anomalies
        gc.collect()

    def aggregate_store(store):
        output = {}

        for region_name in REGION_ORDER:
            means = []
            standard_errors = []

            for lag in LAGS:
                values = store[region_name][lag]

                if len(values) >= 2:
                    means.append(np.mean(values))
                    standard_errors.append(sem(values))
                else:
                    means.append(np.nan)
                    standard_errors.append(np.nan)

            output[region_name] = (
                np.array(means),
                np.array(standard_errors),
            )

        return output

    period1_raw = aggregate_store(data_store["p1_raw"])
    period2_raw = aggregate_store(data_store["p2_raw"])
    period1_detrended = aggregate_store(data_store["p1_det"])
    period2_detrended = aggregate_store(data_store["p2_det"])

    del data_store
    gc.collect()

    return (
        period1_raw,
        period2_raw,
        period1_detrended,
        period2_detrended,
    )


