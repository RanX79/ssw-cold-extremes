def read_ssw_events():
    """Read SEAS5 or ERA5 SSW dates."""
    source = SSW_SOURCE.upper()

    if source == "SEAS5":
        if not SEAS5_SSW_CSV_PATH.exists():
            raise FileNotFoundError(
                f"SEAS5 SSW-event CSV was not found:\n"
                f"  {SEAS5_SSW_CSV_PATH}\n\n"
                "Run 02_seas5_ssw_dates.py first or update "
                "SEAS5_SSW_CSV_PATH."
            )

        df = pd.read_csv(SEAS5_SSW_CSV_PATH)

        required_columns = ("init_year", "member", "ssw_date")
        for column in required_columns:
            if column not in df.columns:
                raise ValueError(f"SEAS5 SSW CSV missing column: {column}")

        df["ssw_date"] = pd.to_datetime(
            df["ssw_date"],
            errors="coerce",
        ).dt.normalize()

        df = df.dropna(subset=["ssw_date"]).copy()

        df = df[
            (df["init_year"] >= START_YEAR)
            & (df["init_year"] <= END_YEAR)
        ].copy()

        df["member"] = df["member"].astype(int)

        df = df.sort_values(
            ["init_year", "member", "ssw_date"]
        ).reset_index(drop=True)

        print(f"Loaded SEAS5 SSW events: {len(df)}")
        return df

    if source == "ERA5":
        if not ERA5_SSW_CSV_PATH.exists():
            raise FileNotFoundError(
                f"ERA5 SSW-event CSV was not found:\n"
                f"  {ERA5_SSW_CSV_PATH}\n\n"
                "Run 01_era5_ssw_dates.py first or update "
                "ERA5_SSW_CSV_PATH."
            )

        era5_df = pd.read_csv(ERA5_SSW_CSV_PATH)

        required_columns = ("init_year", "ssw_date")
        for column in required_columns:
            if column not in era5_df.columns:
                raise ValueError(f"ERA5 SSW CSV missing column: {column}")

        era5_df["ssw_date"] = pd.to_datetime(
            era5_df["ssw_date"],
            errors="coerce",
        ).dt.normalize()

        era5_df = era5_df.dropna(subset=["ssw_date"]).copy()

        era5_df = era5_df[
            (era5_df["init_year"] >= START_YEAR)
            & (era5_df["init_year"] <= END_YEAR)
        ].copy()

        records = []

        for _, row in era5_df.iterrows():
            for member in range(N_MEMBERS):
                records.append(
                    {
                        "init_year": int(row["init_year"]),
                        "member": member,
                        "ssw_date": pd.Timestamp(
                            row["ssw_date"]
                        ).normalize(),
                    }
                )

        df = pd.DataFrame(records)

        if df.empty:
            raise RuntimeError("No ERA5 SSW events are available.")

        df = df.sort_values(
            ["init_year", "member", "ssw_date"]
        ).reset_index(drop=True)

        print(f"Loaded ERA5 SSW events expanded to members: {len(df)}")
        return df

    raise ValueError("SSW_SOURCE must be either 'SEAS5' or 'ERA5'.")


