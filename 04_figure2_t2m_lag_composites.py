def get_var_name(ds, candidates):
    """Identify the 2-m temperature variable in a dataset."""
    for variable_name in candidates:
        if variable_name in ds.data_vars:
            return variable_name

    if len(ds.data_vars) == 1:
        return list(ds.data_vars)[0]

    raise ValueError(
        f"Cannot identify T2m variable. "
        f"Available variables: {list(ds.data_vars)}"
    )


def get_lat_lon_dim_names(da):
    """Return latitude and longitude dimension names."""
    lat_candidates = [dim for dim in da.dims if "lat" in dim.lower()]
    lon_candidates = [dim for dim in da.dims if "lon" in dim.lower()]

    if not lat_candidates:
        raise ValueError(f"Latitude dimension not found: {da.dims}")
    if not lon_candidates:
        raise ValueError(f"Longitude dimension not found: {da.dims}")

    return lat_candidates[0], lon_candidates[0]


def get_time_dim_name(da):
    """Return the time dimension name."""
    for name in ("valid_time", "time"):
        if name in da.dims:
            return name

    raise ValueError(f"Time dimension not found: {da.dims}")


def safe_member_label(number_coord, index):
    """Return an integer member label where possible."""
    try:
        return int(number_coord[index])
    except (TypeError, ValueError):
        return index + 1


def month_day_key(timestamp):
    """Return a calendar-day key in MM-DD format."""
    timestamp = pd.Timestamp(timestamp)
    return f"{timestamp.month:02d}-{timestamp.day:02d}"


def cosine_weighted_region_mean(field_2d, lat_values, lon_values, region_box):
    """Calculate a cosine-latitude-weighted regional mean."""
    lat_mask = (
        (lat_values >= region_box["lat_min"])
        & (lat_values <= region_box["lat_max"])
    )
    lon_mask = (
        (lon_values >= region_box["lon_min"])
        & (lon_values <= region_box["lon_max"])
    )

    if not np.any(lat_mask) or not np.any(lon_mask):
        return np.nan

    subset = field_2d[np.ix_(lat_mask, lon_mask)]

    latitude_weights = np.cos(np.deg2rad(lat_values[lat_mask]))
    weights_2d = latitude_weights[:, None] * np.ones(lon_mask.sum())

    valid = np.isfinite(subset)

    if not np.any(valid):
        return np.nan

    return float(
        np.nansum(subset * weights_2d * valid)
        / np.nansum(weights_2d * valid)
    )


