def load_one_year_daily(year):
    """Load and standardize one daily SEAS5 2-m temperature file."""
    input_file = T2M_DAILY_DIR / (
        f"SEAS5_2mt_NH_{year}11_system51_m25_daily.nc"
    )

    if not input_file.exists():
        print(f"  Missing input file: {input_file.name}")
        return None

    try:
        ds = xr.open_dataset(input_file)

        try:
            variable_name = get_var_name(ds, T2M_VAR_CANDIDATES)
            da = ds[variable_name].load()
        finally:
            ds.close()

        if "number" not in da.dims:
            raise ValueError(
                f"Ensemble-member dimension 'number' not found: {da.dims}"
            )

        da = da.transpose(
            "number",
            *[dimension for dimension in da.dims if dimension != "number"],
        )
        da = da.isel(number=slice(0, N_MEMBERS))

        lat_name, lon_name = get_lat_lon_dim_names(da)
        time_name = get_time_dim_name(da)

        if da[lat_name].values[0] < da[lat_name].values[-1]:
            da = da.isel({lat_name: slice(None, None, -1)})

        original_lon = da[lon_name].values

        if np.nanmax(original_lon) > 180:
            converted_lon = np.where(
                original_lon > 180,
                original_lon - 360,
                original_lon,
            )
            sort_index = np.argsort(converted_lon)

            da = da.isel({lon_name: sort_index})
            da = da.assign_coords({lon_name: converted_lon[sort_index]})

        times = pd.to_datetime(da[time_name].values).normalize()

        member_labels = [
            safe_member_label(da["number"].values, index)
            for index in range(da.sizes["number"])
        ]

        t2m_values = da.values.astype(np.float32)
        lat_values = da[lat_name].values.copy()
        lon_values = da[lon_name].values.copy()

        print(f"  Loaded: {input_file.name}; shape={t2m_values.shape}")

        return {
            "t2m": t2m_values,
            "times": times,
            "monthday": np.array([month_day_key(time) for time in times]),
            "member_labels": member_labels,
            "member_to_idx": {
                member: index
                for index, member in enumerate(member_labels)
            },
            "time_to_idx": {
                pd.Timestamp(time): index
                for index, time in enumerate(times)
            },
            "lat_vals": lat_values,
            "lon_vals": lon_values,
        }

    except Exception as error:
        print(f"  Failed to load initialization year {year}: {error}")
        gc.collect()
        return None


