def save_lag_cache(
    npz_path,
    period1_raw,
    period2_raw,
    period1_detrended,
    period2_detrended,
):
    """Save lag composite results to an NPZ archive."""
    output = {}

    result_groups = (
        ("r1raw", period1_raw),
        ("r2raw", period2_raw),
        ("r1det", period1_detrended),
        ("r2det", period2_detrended),
    )

    for region_name in REGION_ORDER:
        for tag, result in result_groups:
            output[f"{region_name}__{tag}__mean"] = result[region_name][0]
            output[f"{region_name}__{tag}__sem"] = result[region_name][1]

    np.savez(npz_path, **output)
    print(f"Saved lag-composite cache: {npz_path}")


def load_lag_cache(npz_path):
    """Load lag-composite data from an NPZ archive."""
    if not npz_path.exists():
        raise FileNotFoundError(f"Lag cache not found: {npz_path}")

    data = np.load(npz_path, allow_pickle=False)

    def load_group(tag):
        return {
            region_name: (
                data[f"{region_name}__{tag}__mean"],
                data[f"{region_name}__{tag}__sem"],
            )
            for region_name in REGION_ORDER
        }

    print(f"Loaded lag-composite cache: {npz_path}")

    return (
        load_group("r1raw"),
        load_group("r2raw"),
        load_group("r1det"),
        load_group("r2det"),
    )


